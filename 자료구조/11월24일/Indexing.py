# 2차원 리스트 (행렬처럼 생각)
matrix = [
    [1, 2, 3],  # 인덱스 0
    [4, 5, 6],  # 인덱스 1
    [7, 8, 9]   # 인덱스 2
]

# 인덱싱 예시:
# 1. matrix[1] : [4, 5, 6] (첫 번째 리스트 인덱스)
# 2. matrix[1][0]: 4 (두 번째 리스트 인덱스)
print(f"중첩 인덱싱 [1][0] 결과: {matrix[1][0]}") # 출력: 4

# 인덱싱 예시 2:
# 1. matrix[2] : [7, 8, 9]
# 2. matrix[2][-1]: 9
print(f"중첩 인덱싱 [2][-1] 결과: {matrix[2][-1]}") # 출력: 9